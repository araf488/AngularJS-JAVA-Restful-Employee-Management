package entity;

/**
 *
 * @author araf0
 */
public class Salary {
    int id;
    String ename;
    String month;
    int year;
    double salary;
    String status;

    public Salary() {
        super();
    }

    public Salary(int id, String ename, String month, int year, double salary, String status) {
        super();
        this.id = id;
        this.ename = ename;
        this.month = month;
        this.year = year;
        this.salary = salary;
        this.status = status;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getEname() {
        return ename;
    }

    public void setEname(String ename) {
        this.ename = ename;
    }

    public String getMonth() {
        return month;
    }

    public void setMonth(String month) {
        this.month = month;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public double getSalary() {
        return salary;
    }

    public void setSalary(double salary) {
        this.salary = salary;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
